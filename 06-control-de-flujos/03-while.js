// WHILE
// Repite un bloque de código mientras la condición sea verdadera

let contador = 0;

while (contador < 5) {
  console.log("🔁 Contador:", contador);
  contador++; // ¡Importante! Hay que actualizar la condición dentro del bucle
}

// ⚠️ si la condición nunca cambia, se crea un bucle infinito!!!
